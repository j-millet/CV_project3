def get_output_images(model, dataloader, device, threshold):
    label_names = ['background', 'person', 'rider', 'car', 'truck', 'bus', 
                   'caravan', 'trailer', 'train', 'motorcycle', 'bicycle']
    class_colors = {
        1: "red",        # person
        2: "orange",     # rider
        3: "green",      # car
        4: "blue",       # truck
        5: "purple",     # bus
        6: "brown",      # caravan
        7: "pink",       # trailer
        8: "cyan",       # train
        9: "yellow",     # motorcycle
        10: "magenta"    # bicycle
    }

    images, masks = next(iter(dataloader))
    output_images = []

    model.eval()
    for image in images:
        with torch.no_grad():
            x = image.clone()
            # Convert RGBA -> RGB and move to device
            x = x[:3, ...].to(device)
            predictions = model([x, ])
            pred = predictions[0]

        image = (255.0 * (image - image.min()) / (image.max() - image.min())).to(torch.uint8)
        image = image[:3, ...]

        filtered_preds = []
        filtered_masks = []
        filtered_colors = []
        filtered_boxes = []
        for label, score, mask, box in zip(pred["labels"], pred["scores"], pred["masks"], pred["boxes"]):
            if score > threshold:
                filtered_preds.append((label, score))
                filtered_masks.append(mask > threshold)
                filtered_colors.append(class_colors[label.item()])
                filtered_boxes.append(box.long())

        pred_labels = [f"{label_names[label]}: {score:.3f}" for label, score in filtered_preds]
        
        if filtered_boxes:
            image = draw_bounding_boxes(image, torch.stack(filtered_boxes).long(), pred_labels, colors="red")

        if filtered_masks:
            image = draw_segmentation_masks(
                image,
                torch.stack(filtered_masks).squeeze(1),
                alpha=0.5,
                colors=filtered_colors
            )

        output_images.append(image)

    return output_images


def setup_rcnn(path=None, device='cuda'):
    model = maskrcnn_resnet50_fpn()
    num_classes = 11
    in_features = model.roi_heads.box_predictor.cls_score.in_features
    model.roi_heads.box_predictor = FastRCNNPredictor(in_features, num_classes)
    in_features_mask = model.roi_heads.mask_predictor.conv5_mask.in_channels
    hidden_layer = 256
    model.roi_heads.mask_predictor = MaskRCNNPredictor(in_features_mask, hidden_layer, num_classes)
    
    if path: model.load_state_dict(torch.load(path, weights_only=True,map_location=device))
    model.to(device)
    return model

def setup_instance_seg(path=None, device='cuda'):
    model = SimpleInstanceSeg(num_classes=11)
    if path: model.load_state_dict(torch.load(path, weights_only=True,map_location=device))
    model.to(device)
    return model

def load_model_for_inference(model_type, path,device='cuda'):
    model = None
    match model_type:
        case "mask_rcnn":
            model = setup_rcnn(path,device)
        case "instance_seg":
            model = setup_instance_seg(path,device)
    return model
            

def tensorboard_save_imgs(model, writer, epoch, batch_id, dataloader, device, threshold):
    images = get_output_images(model, dataloader, device, threshold)
    for i, img in enumerate(images):
        writer.add_image(f"Results/Epoch_{epoch}_Batch_{batch_id}_Image_{i}", img, epoch)

